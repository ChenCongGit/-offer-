# include <iostream>
using namespace std;


struct ListNode
{
    int m_nValue;
    ListNode* m_pNext;
    ListNode(int value=0, ListNode* next=nullptr): m_nValue(value), m_pNext(next) {}
};
ListNode* CreateListNode(int value);
void ConnectListNodes(ListNode* pCurrent, ListNode* pNext);
void PrintListNode(ListNode* pNode);
void PrintList(ListNode* pHead);
void DestroyList(ListNode* pHead);
void AddToTail(ListNode** pHead, int value);
void RemoveNode(ListNode** pHead, int value);


ListNode* Merge(ListNode* pHead1, ListNode* pHead2)
{
    // 特殊输入
    if (pHead1 == nullptr) return pHead2;
    if (pHead2 == nullptr) return pHead1;

    // 定义两个指针
    ListNode* p1 = pHead1;
    ListNode* p2 = pHead2;
    ListNode* pFinal = new ListNode(-1,nullptr);
    ListNode* pMain = pFinal;

    while (true)
    {
        if (p1->m_nValue < p2->m_nValue)
        {
            pMain->m_pNext = p1;
            p1 = p1->m_pNext;
        }
        else
        {
            pMain->m_pNext = p2;
            p2 = p2->m_pNext;
        }
        pMain = pMain->m_pNext;
        // 链表1走到最后
        if (p1 == nullptr)
        {
            pMain->m_pNext = p2; break;
        }
        if (p2 == nullptr)
        {
            pMain->m_pNext = p1; break;
        }
    }
    return pFinal->m_pNext;
}


